async function readContent<T>(key: string, fallback: T): Promise<T> {
  try {
    const service = createService()
    const { data } = await service.from('site_content').select('data').eq('key', key).maybeSingle()
    if (data?.data) return { ...fallback, ...(data.data as Partial<T>) }
  } catch {
    // テーブル未作成・到達不可時はデフォルト
  }
  return fallback
}

